                    <h2>1962</h2>
                    <p>
                    İkinci Dünya Savaşı’nın bitişinden sonra yeniden canlanmaya başlayan Dünya ticaretinin ülkemize yansımaları 1950’li yıllarda kendini hissettirmeye başladı. Türkiye’nin çehresi değişiyordu; Fabrikalar ve barajlar yapılıyor, şehirlerarası yollar açılıyordu. Gün geçtikçe yayılan ve gelişen karayolu ağı beraberinde şehirlerarası ulaşım ihtiyacını doğurdu. Demir yollarının ulaşamadığı kentler arasında yolcu taşınıyordu. Bu hareket Ege’de bazı oluşumları da beraberinde getirdi. Geçmişi Cumhuriyet’in ilk yıllarına kadar uzanan Hüseyin Mazlum Bababalım’ın Atatürk’ten onay aldığı projesi, 1962 yılında torunları tarafından hayata geçirildi. Adını Kocaeli’nin doğa harikası (Hierapolis) Umuttepe’den alan ‘Umuttepe Turizm’ Bababalım ailesi tarafından kuruldu. Tarih 16 Ocak 1962’idi.
