                    <h2>2009</h2>
                    <p>
                    Umuttepe Turizm ailesi olarak kendimizi “toplumun kurumsal bir vatandaşı” olarak görüyoruz. Kuruluşumuzdan bu yana hayata geçirdiğimiz sosyal sorumluluk ve destek projelerini 2009 yılında “Umuteller” markası çatısında topladık. Eğitim, sanat, sağlık ve spor alanında birçok projeyi hayata geçirdik. 2009 ve 2010 yıllarında Ulaştırma sektörü birincisi seçilerek, sektörde öncü ve örnek adımlar atmayı sürdürme hedefimiz ile sektörün geleceğini emin ellere bırakmayı sorumluluk haline getirerek “Umuttepe Akademi” ‘yi kurduk. Sektörde uzmanlık kazanmak isteyen gençlere eğitim desteği vererek, “Yaşam Boyu Eğitim” kavramını temel felsefemiz haline getirdik.
