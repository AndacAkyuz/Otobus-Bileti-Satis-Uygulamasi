                    <h2>2019</h2>
                    <p>
                    ”Kalite, ülkemizin güzel insanlarının en doğal hakkıdır” anlayışını ilke edinmiş bir markayız. Bu ilkemizden hareketle; 2019 yılında “JUMBO’ya Terfi Edin, Standartlarınız Yükselsin” sloganımız ile özel donanımlar ve hizmetler ile geliştirdiğimiz ayrıcalıklı sefer modelimiz “JUMBO” seferlerine başladık.
                    “JUMBO” araçlarımız 75 cm. koltuk arası mesafesi ile Türkiye’deki en geniş koltuk aralığına sahip tek araç olma özelliğini taşıyor. Ayrıca baldır dayama, ayak koyma aparatları ve koltukların geniş açıda yatırılma özellikleri ile daha keyifli yolculuk ve konforlu uyku imkânı sağlıyoruz.Yolcularımızın tüm ihtiyaçlarını düşünerek tasarladığımız “JUMBO” araçlarımızda genişletilmiş ikram menüsü ve ek hizmetler farklılık yaratıyor.<br><br>
                    
