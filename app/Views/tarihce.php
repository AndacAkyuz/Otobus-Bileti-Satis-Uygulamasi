                    <h2>2011</h2>
                    <p>
                    2011 yılında en büyük yatırımımızı sanayi şehri Bursa’ya üs kurarak yaptık. Büyüme stratejimiz çerçevesinde Türkiye ekonomisine yön veren Bursa’dan, faaliyet gösterdiğimiz Akdeniz, Ege, Batı Karadeniz, İç Anadolu, Marmara bölgelerimize direkt kalkışlı seferler düzenlemeye başlayarak Bursa operasyonunda çalışacak otobüslerimiz için 9 milyon Avro yatırım gerçekleştirdik. 
                    2011 yılından bu yana büyümeyi hızlandırarak, ulaşım ağını genişletmeye devam ederek Ege ve Akdeniz bölgelerindeki başarımızı Marmara Bölgesine de taşıyarak iç turizmin yoğun olduğu Bandırma, Erdek ve Karacabey hatlarını açtık.
                    2014’de “Adım Adım Trakya” projemiz kapsamında başlattığımız seferlerimize, Trakya Bölgesi’nin önemli yerleşim yerlerinden olan Kırklareli, Lüleburgaz ve Çorlu’yu güzergâh haritamıza ekledik. Tekirdağ ve Keşan’ı da renklerine bağlayarak bir adım daha attık.
                    </p>	
