                    <h2>2005</h2>
                    <p>
                    2004 - 2005 yıllarına gelindiğinde neredeyse yarım asırlık tecrübemizi yeni kurumsal kimliğimiz ve profesyonel organizasyon yapımız ile birleştirerek sektöre yön verenlerden biri olarak yolumuza devam ettik. 2006 yılında müşteri ilişkileri yönetimi alanındaki yenilikleri, kendi sektörümüze taşıyarak günümüzde 2,5 milyon aktif kullanıcıya ulaşan “Umutkart” uygulaması ile yolcu sadakat projesini başlattık.Teknolojinin yaşam tarzına da yansıması kaçınılmazdı. Bu doğrultuda bireysel insan vurgusunun da öne çıkması sektörde yeni bir bakış açısı oluşturdu. 2+1 genişletilmiş özel koltuk dizaynı, her koltukta LCD ekran, uydu kanal yayını, bilgisayar oyunları, kişisel özel yayın imkânı, kesintisiz mobil hızlı internet gibi çağımızın olmazsa olmazları “Umutyol” çatısı altında birleştik. Umutyol ve Teknobüs araç donanımlarına, koltuk arkası ekranlarda kullanabilecekleri kişisel USB uygulamasını getirdik. “Kontrol Sizde” sloganı ile bu uygulamayı tanıtarak, yolcularımızın kendi tercih ettikleri ve diledikleri zaman ileri/geri alabilecekleri film, müzik, fotoğraf gibi dokümanlarını seyahat esnasında kullanabilmelerini sağladık. Koltuk arkası kişisel ekranlar halen donanımsal ve yazılımsal olarak geliştirmeye devam etmekte ve yeni özellikler kazandırmaktayız.
