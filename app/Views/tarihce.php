                    <h2>2016</h2>
                    <p>
                    2016 yılında 100 adetlik 2+1 Umutyol otobüs alımını tamamlayarak, 2017 yılına da yine filomuza 100 adet otobüs dâhil ederek başladık. 10 inç koltuk arkası ekranları, ücretiz internet hizmeti, şube ve tesislerin altyapısı, geniş servis ağı, çok seçenekli ikram ve filomuzun tümünde her koltukta bulunan prizlerimiz ile çalışmalarımızı “Seyahat Etmek Özgürlüktür” temasından yola çıkarak kurguluyoruz. Bu yüksek donanıma sahip Umutyol ve Teknobüs araçlarımızdan oluşan 500 araçlık filo ile 2016 yılını 13 milyon yolcumuzu sevdiklerine kavuşturarak tamamladık.
                    </p><br>&nbsp;<br>&nbsp;<br>&nbsp;<br>&nbsp;
