              <p>Aşı karneli kedi, köpek (tehlike arz edenler hariç) ve kuş (saka, muhabbet kuşu veya kanarya) gibi 8 kg. ağırlığın altındaki evcil hayvanlarımız, özel taşıma kafeslerinde (23x30x40 cm. ölçülerinden küçük kafeslerde) kilitli olmak şartıyla, hayvan sahibi yolcumuzun kucağında seyahat edebilir.Kucakta taşınabilme ağırlık ve ölçü limitleri, taşıyıcı yolcunun ve yanındaki yolcunun seyahat güvenliği gözetilerek belirlenmiştir. Hayvan sahibi yolcumuz, evcil hayvanı için dilerse standart fiyat tarifesine uygun olarak koltuk satın alabilir.</p>
