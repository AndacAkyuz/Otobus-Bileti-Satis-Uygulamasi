<b>III.	Rızaya Tabi İşlemeler</b>
<p>
<b>Çerez Verileri:</b> Web sitemiz çerezleri kullanmaktadır. “Çerez” tarayıcınıza gönderilebilen bir veridir. Tarayıcınız belirlediğiniz seçenekler doğrultusunda çerezleri sisteminizde saklayabilir. Genel olarak internet tarayıcıları, çerezleri otomatik olarak kabul edecek şekilde ön tanımlıdır. Tarayıcılar, çerezleri engelleyecek veya cihaza çerez gönderildiğinde kullanıcıya uyarı verecek şekilde ayarlayabilirsiniz. Çerez Politikamız doğrultusunda pazarlama verileriniz, profilinizi oluşturmak ve ilgi alanlarınızla alakalı reklamlar sunmak amacıyla işlenebilecek ve reklam çerezleri Google Şirketine aktarılabilecektir. Daha fazla bilgi için Google’ın gizlilik politikasını inceleyebilirsiniz. Konuya ilişkin detaylı bilgiye ÇEREZ POLİTİKASI linkinden ulaşabilirsiniz. Çerezleri yönetmek tarayıcıdan tarayıcıya farklılık gösterdiğinden ayrıntılı bilgi almak için tarayıcının yardım menüsüne bakılabilir. İnternet tarayıcınızdaki ayarları değiştirerek çerezleri kabul veya reddetme imkanına sahipsiniz. Tarayıcı ayarlarınızı nasıl değiştireceğinizi ya da çerezleri nasıl sileceğinizi öğrenmek için lütfen tarayıcınızın yönergelerine bakınız. Çerez Verilerinizin yapılan aydınlatma, bu şirketin ortakları, iş ortakları, halefleri ve/veya bunların belirleyeceği üçüncü kişiler tarafından, yasal nedenlerle otomatik olan/olmayan yöntemler ile alınmasına, yasal süreler aşılmamak üzere yurt içinde ve/veya yurt dışında yazılı/dijital arşivlere kaydedilmesine, depolanmasına, muhafaza edilmesine, kullanılmasına, güncellenmesine, değiştirilmesine, yeniden düzenlenmesine, sınıflandırılmasına, yurt içinde veya yurt dışına aktarılmasına ve sair kanunlara uygun şekilde işlenmesine, ilgili tüm yasal haklarınız hakkında bilgilendirilmiş olarak açıkça rıza göstermek istiyorsanız çerezleri kabul etmeniz yeterlidir.
</p><p>
<b>İletişim verileri:</b> Verebileceğiniz rıza doğrultusunda kampanya bildirimleri ve çapraz alışveriş fırsatlarının paylaşılması amacı dahil olmak üzere ürün ve/veya hizmetlerin tanıtımı, promosyonu, size özel kampanya, reklam, hedefleme, analiz vs. gibi amaçlarla işlenebilecek, saklanabilecektir. Bu kapsamda Umuttepe grup şirketleri ile, Şirket’in iş ortakları ile hukuken yetkili kamu kurum ve kuruluşları ile paylaşılabilecektir. 6563 sayılı Elektronik Ticaretin Düzenlenmesi Hakkında Kanununa uygun olarak iletişim verilerinizin kampanya bildirimleri almak ve size özel tekliflerin sunulabilmesi, memnuniyet takibi, duyuru reklam, promosyon ve kampanyalar hakkında ticari elektronik iletiler gönderilerek iletişime geçilmesine ve bilgilerinizin bu amaçla kullanılmasına, bu şirketin ortakları, iş ortakları, halefleri ve/veya bunların belirleyeceği üçüncü kişiler tarafından, yasal nedenlerle otomatik olan/olmayan yöntemler ile alınmasına, yasal süreler aşılmamak üzere yurt içinde yazılı/dijital arşivlere kaydedilmesine, depolanmasına, muhafaza edilmesine, kullanılmasına, güncellenmesine, değiştirilmesine, yeniden düzenlenmesine, sınıflandırılmasına, yurt içinde aktarılmasına ve sair kanunlara uygun şekilde işlenmesine, ilgili tüm yasal haklarınız hakkında bilgilendirilmiş olarak açıkça rıza göstermek istiyorsanız Üye Ol sekmesindeki kutucuğu onaylamanız yeterlidir. Verdiğiniz açık rızayı üyelik ayarlarınızdan her zaman güncelleyebilirsiniz.
</p>
<b>IV. Kişisel Verilerinizin Aktarılması</b>
<p>
İşlenen kişisel verileriniz; Kanun tarafından öngörülen temel ilkelere uygun olarak ve Kanun’un  8. ve 9. maddelerinde belirtilen şart ve amaçlar dâhilinde, mevzuat ve politikalarımıza uygun olarak faaliyetlerimizi yürütmek, Şirketimiz tarafından sunulan ürün ve hizmetlerden sizleri faydalandırmak için gerekli çalışmaların iş birimlerimiz tarafından yapılması, Şirketimizin ve Şirketimizle iş ilişkisi içerisinde olan kişilerin hukuki ve ticari güvenliğinin temini (Şirketimiz tarafından yürütülen iletişime yönelik idari operasyonlar, Şirkete ait lokasyonların fiziksel güvenliğini ve denetimini sağlamak, iş ortağı/müşteri/tedarikçi (yetkili veya çalışanları) değerlendirme süreçleri, hukuki uyum süreci, mali işler v.b.), Şirketimizin ticari ve iş stratejilerinin belirlenmesi ve uygulanması ile Şirketimizin insan kaynakları politikalarının yürütülmesinin temini amaçlarıyla iş birimlerimizin bu doğrultuda gerekli çalışmalarını yapması için, Şirketlerimiz bünyesindeki çalışanlarımızla paylaşılabileceği gibi, şubelerimiz, acentelerimiz, tedarikçilerimiz, İlgili diğer mevzuat hükümlerinin izin verdiği kişi/kurum ve/veya kuruluşlar, özel sigorta şirketleri, bankalar, özel hukuk gerçek ve tüzel kişileri, hukuki işlerin takibi amacıyla avukatlar veya avukatlık ortaklıkları, finans ve muhasebe işlemlerinin yürütülmesi amacıyla mali müşavirlere aktarılabilecektir. Bunların dışında rıza göstermeniz halinde “Rızaya Tabi İşlemler” başlığı altındaki çerez verileriniz Google’a aktarılabilecektir.
</p>
<b>V. Kişisel Verilerin İşlenme Süresi ve Güvenliği</b>
<p>
Umuttepe Grup Şirketleri, kişisel verileri işbu Kişisel Verilerin İşlenmesine İlişkin Aydınlatma Metni’nde belirtilen amaçların ve ilgili mevzuatın gerektirdiği süre boyunca saklayacaktır. Buna ek olarak, Umuttepe Grup Şirketleri, veri sahibi ile arasında doğabilecek herhangi bir uyuşmazlık durumunda, uyuşmazlık kapsamında gerekli savunmaların gerçekleştirilebilmesi amacıyla sınırlı olmak üzere ve ilgili mevzuat uyarınca belirlenen zamanaşımı süreleri boyunca kişisel verileri saklayabilecektir.
