<h3>KİŞİSEL VERİLERİN İŞLENMESİNE İLİŞKİN AYDINLATMA METNİ</h3>
<p>
Umuttepe Turizm Sanayi Akaryakıt Tic. ve Ltd. Şti, A Umuttepe Taşımacılık Turizm Tic Ltd Şti., A Umuttepe Kargo Lojistik ve Taşımacılık A.Ş., Umuttepe Taşımacılık Turizm Tic. A. Ş. Anadolu Bilet Satış Hiz. Ve Oto. İşl. A. Ş. (birlikte “Umuttepe” olarak anılacaktır) olarak kişisel verilerinizin korunmasına ve güvenliğine azami özen göstermekteyiz. Bu kapsamda, başta özel hayatın gizliliği olmak üzere kişilerin temel hak ve özgürlüklerini korumak ve kişisel verilerin korunması amacıyla düzenlenen 6698 sayılı Kişisel Verilerin Korunması Kanunu (“Kanun”) ve ilgili mevzuat uyarınca kişisel verilerinizin toplanma şekilleri, işlenme amaçları, işlemenin hukuki nedenleri ve haklarınız konularında Veri Sorumlusu sıfatıyla sizleri en şeffaf şekilde aydınlatmak ve bilgilendirmek isteriz. 
<br>
</p>
<b>I. Kişisel Verilerinizin Toplanma Yöntemleri</b>
<p>Kişisel verileriniz, Umuttepe tarafından verilen hizmet, ürün ya da faaliyete bağlı olarak değişkenlik gösterebilmekle; otomatik ya da otomatik olmayan yöntemlerle, ofis birimleri, acenteler, çağrı merkezi, bilet satış noktaları, internet sitesi, sosyal medya mecraları, mobil uygulamalar, etkinlikler, fuar, tanıtım ve benzeri vasıtalarla sözlü, yazılı ya da elektronik olarak toplanmaktadır.</p><p>
Kişisel verileriniz, Umuttepe olarak faaliyetlerimizi icra ederken; Umuttepe tarafından farklı kanallarla mevzuata, politikalarımıza ve üstlenilen yükümlülüklere eksiksiz bir şekilde uyum sağlanabilmesi amacıyla toplanmaktadır. 
</p>
<b>II. Kişisel Verilerinizin İşlenmesi ve Hukuki Sebebi</b>
<p>
Şirketimizin sunmuş olduğu ürün ve hizmetlerden faydalanmanız sırasında aşağıda detaylı şekilde açıklanan kişisel verileriniz, Kanunun 4. maddesinde öngörülen temel ilkelere uygun olarak, faaliyetlerimizin yürütülebilmesi ve hukuki yükümlülüklerin yerine getirilmesi başta olmak üzere Kanun’un 5. ve 6. maddelerinde belirtilen kişisel veri işleme şartları ve öngörülen düzenlemeler kapsamında işlenebilmekte ve aktarılabilmektedir.
</p>
